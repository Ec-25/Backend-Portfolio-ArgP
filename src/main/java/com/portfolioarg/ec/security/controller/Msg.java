package com.portfolioarg.ec.security.controller;

public class Msg {
    private String msg;

    //Contructor
    public Msg() {
    }

    public Msg(String msg) {
        this.msg = msg;
    }

    //Getters & Setters
    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
